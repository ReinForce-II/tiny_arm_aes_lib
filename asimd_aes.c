static const uint8_t rc[11] = {0x00, 0x01, 0x02, 0x04, 0x08, 0x10,
                               0x20, 0x40, 0x80, 0x1B, 0x36};

static uint8x16_t asimd_aes_enc_core(const uint8x16_t keys[], uint8x16_t data) {
    for (uint32_t i = 0; i < 9; ++i) {
        data = vaeseq_u8(data, keys[i]);
        data = vaesmcq_u8(data);
    }
    data = vaeseq_u8(data, keys[9]);
    data = veorq_u8(data, keys[10]);
    return data;
}

void asimd_aes_enc_ecb(const uint8x16_t rdkeys[], const uint8_t input[],
                       uint8_t output[], uint32_t length) {
    while (length >= 16) {
        uint8x16_t block = vld1q_u8(input);
        block = asimd_aes_enc_core(rdkeys, block);
        vst1q_u8(output, block);
        input += 16;
        output += 16;
        length -= 16;
    }
}

void asimd_aes_enc_cbc(const uint8x16_t rdkeys[], const uint8_t iv[],
                       const uint8_t input[], uint8_t output[],
                       uint32_t length) {
    uint8x16_t __iv;
    __iv = vld1q_u8(iv);

    while (length >= 16) {
        uint8x16_t block = veorq_u8(vld1q_u8(input), __iv);
        block = asimd_aes_enc_core(rdkeys, block);
        vst1q_u8(output, block);
        __iv = block;
        input += 16;
        output += 16;
        length -= 16;
    }
}

void asimd_aes_enc_cfb(const uint8x16_t rdkeys[], const uint8_t iv[],
                       const uint8_t input[], uint8_t output[],
                       uint32_t length) {
    uint8x16_t __iv;
    __iv = vld1q_u8(iv);

    while (length >= 16) {
        __iv = asimd_aes_enc_core(rdkeys, __iv);
        __iv = veorq_u8(vld1q_u8(input), __iv);
        vst1q_u8(output, __iv);
        input += 16;
        output += 16;
        length -= 16;
    }
}

void asimd_aes_enc_cfb8(const uint8x16_t rdkeys[], const uint8_t iv[],
                        const uint8_t input[], uint8_t output[],
                        uint32_t length) {
    uint8x16_t __iv;
    uint8_t __tmp[16];
    uint8_t c;
    uint8_t ov[17];
    memcpy(__tmp, iv, 16);
    while (length--) {
        memcpy(ov, __tmp, 16);
        __iv = vld1q_u8(__tmp);
        __iv = asimd_aes_enc_core(rdkeys, __iv);
        vst1q_u8(__tmp, __iv);
        c = __tmp[0] ^ *input++;
        *output++ = c;
        ov[16] = c;
        memcpy(__tmp, ov + 1, 16);
    }
}

static uint32_t rotword(uint32_t w) {
    uint32_t r;
    uint8_t *_w = (uint8_t *)&w;
    uint8_t *_r = (uint8_t *)&r;
    _r[0] = _w[1];
    _r[1] = _w[2];
    _r[2] = _w[3];
    _r[3] = _w[0];
    return r;
}

static uint32_t subword(uint32_t w) {
    uint32_t r;
    uint8_t *_w = (uint8_t *)&w;
    uint8_t *_r = (uint8_t *)&r;
    _r[0] = sbox[_w[0] >> 4][_w[0] & 0xf];
    _r[1] = sbox[_w[1] >> 4][_w[1] & 0xf];
    _r[2] = sbox[_w[2] >> 4][_w[2] & 0xf];
    _r[3] = sbox[_w[3] >> 4][_w[3] & 0xf];
    return r;
}

void aes_key_schedule(uint32_t *key, uint32_t *rdkey) {
    rdkey[0] = key[0];
    rdkey[1] = key[1];
    rdkey[2] = key[2];
    rdkey[3] = key[3];
    for (uint32_t i = 4; i < 44; i += 1) {
        if (i % 4 == 0) {
            rdkey[i] =
                rdkey[i - 4] ^ subword(rotword(rdkey[i - 1])) ^ rc[i / 4];
        } else {
            rdkey[i] = rdkey[i - 4] ^ rdkey[i - 1];
        }
    }
}
